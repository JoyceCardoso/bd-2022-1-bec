CREATE TABLE AGRICULTOR (
CodA int NOT NULL,
NomeA VARCHAR(100) NOT NULL,
CidadeA VARCHAR(100) NOT NULL,
PRIMARY KEY (CodA));

CREATE TABLE PRODUTO (
CodP int NOT NULL,
NomeP VARCHAR(20) NOT NULL,
PrecoQuilo NUMERIC(20,2) NOT NULL,
PRIMARY KEY (CodP),
UNIQUE(NomeP));

CREATE TABLE RESTAURANTE (
CodR int NOT NULL,
NomeR VARCHAR(20) NOT NULL,
Cidade VARCHAR(20) NOT NULL,
PRIMARY KEY (CodR),
UNIQUE(NomeR));

CREATE TABLE ENTREGA (
CodA int NOT NULL,
CodP int NOT NULL,
CodR int NOT NULL,
DataEntrega DATE NOT NULL,
QtdeQuilos INT NOT NULL,
PRIMARY KEY (CodA, CodP, CodR),
FOREIGN KEY (CodA) REFERENCES AGRICULTOR (CodA),
FOREIGN KEY (CodP) REFERENCES PRODUTO (CodP),
FOREIGN KEY (CodR) REFERENCES RESTAURANTE (CodR));

